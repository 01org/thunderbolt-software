#pragma once

#include <stdexcept>

class TbtException:
   public std::runtime_error
{
public:
   // If the given pointer is nullptr, than what() will return "unknown exception".
   TbtException(const char* description = nullptr):
      std::runtime_error(description)
   {
   }

   // Doesn't throw any exceptions
   const char* what() const throw()
   {
      return std::runtime_error::what();
   }
};